package gtf.math.geometry;


/**
 * Variance of a tensor index.
 *
 * <p>
 * A contravariant index is conventionally written as an upper index, while a
 * covariant index is conventionally written as a lower index.
 * </p>
 *
 * @author gtf
 */
public enum TensorVariance {

  /** Upper-index variance. */
  CONTRAVARIANT,

  /** Lower-index variance. */
  COVARIANT
}
